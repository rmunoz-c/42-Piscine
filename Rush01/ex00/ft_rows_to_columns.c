int	**ft_rows_to_columns(int row_mx[4][4])
{
	int	**col_mx;
	int	i;
	int	j;

	i = -1;
	col_mx = malloc(4 * 16);
	while (++i <= 3)
	{
		col_mx[i] = malloc(4 * 4);
	}
	i = -1;
	while (++i <= 3)
	{
		j = -1;
		while (++j <= 3)
		{
			col_mx[j][i] = row_mx[i][j];
		}
	}
	return (col_mx);
}

int	**ft_p_couple(int *p_int, int init)
{
	int	**mx_pm;
	int	i;
	int	j;

	i = -1;
	mx_pm = malloc(8 * 4);
	while (++i <= 3)
	{
		mx_pm[i] = malloc(2 * 4);
	}
	i = -1;
	while (++i <= 3)
	{
		j = -1;
		while (++j <= 1)
		{
			mx_pm[i][j] = p_int[init + i + 4 * j];
		}
	}
	return (mx_pm);
}

int	**ft_mx_pos_rows(int **mx_pm, int i)
{
	int	**rows_mx;
	int	j;
	int	k;
	int	p1;
	int	p2;

	p1 = mx_pm[i][0] - 1;
	p2 = mx_pm[i][1] - 1;
	rows_mx = malloc(5 * 16);
	j = -1;
	while (++j <= 4)
	{
		rows_mx[j] = malloc(4 * 4);
	}
	j = -1;
	while (++j <= 4)
	{
		k = -1;
		while (++k <= 3)
		{
			rows_mx[j][k] = g_rows[p1][p2][j][k];
		}
	}
	return (rows_mx);
}
