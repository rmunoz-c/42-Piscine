#include <unistd.h>

int	ft_is_prime(int nb)
{
	int	i;

	if (nb == 2)
		return (1);
	else if (nb < 2 || nb % 2 == 0)
		return (0);
	i = 0;
	while ((3 + 2 * i) < nb / 3)
	{
		if (nb % (3 + 2 * i) == 0)
			return (0);
		i++;
	}
	return (1);
}
